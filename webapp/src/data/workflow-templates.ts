/**
 * Built-in workflow templates for common ComfyUI pipelines.
 * Each template includes a complete, valid ComfyUI workflow JSON
 * that can be loaded directly into the editor.
 */

export interface WorkflowTemplate {
  id: string;
  name: string;
  description: string;
  category: WorkflowTemplateCategory;
  difficulty: 'beginner' | 'intermediate' | 'advanced';
  tags: string[];
  /** Required architecture/base model */
  baseModel: string;
  /** Node types used (for dependency checking) */
  nodeTypes: string[];
  /** Whether this uses only built-in nodes */
  builtInOnly: boolean;
  /** Required custom node packs (GitHub URLs) */
  requiredPacks?: string[];
  /** The full workflow JSON */
  workflow: Record<string, unknown>;
  /** Thumbnail description (for generating a placeholder) */
  thumbnailHint: string;
}

export type WorkflowTemplateCategory =
  | 'text-to-image'
  | 'image-to-image'
  | 'inpainting'
  | 'upscaling'
  | 'controlnet'
  | 'lora'
  | 'advanced'
  | 'flux'
  | 'video';

